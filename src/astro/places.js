const PLACE_BY_NAME = new Map(PLACES.map((place) => [place.name, place]));

export async function loadPlaces() {
  try {
    const response = await fetch("./data/china-city-geo.raw.json");
    if (!response.ok) throw new Error(`HTTP ${response.status}`);
    const rows = await response.json();
    return dedupePlaces([...PLACES, ...normalizeChinaGeoRows(rows)]);
  } catch (error) {
    console.warn("Failed to load local China place database:", error);
    return PLACES;
  }
}

export function normalizeChinaGeoRows(rows) {
  return rows
    .map((row) => normalizeChinaGeoRow(row))
    .filter(Boolean);
}

export function findPlaceByName(name, places = PLACES) {
  const trimmed = name.trim();
  return places.find((place) => place.name === trimmed) || PLACE_BY_NAME.get(trimmed) || null;
}

export function placeMatches(place, query) {
  const normalized = query.trim().toLowerCase();
  if (!normalized) return true;
  return [place.name, ...place.keywords].some((item) => item.toLowerCase().includes(normalized));
}

export function timeZoneIdForPlace(place) {
  if (place.timeZone) return place.timeZone;
  const name = place.name;
  if (name.includes("日本")) return "Asia/Tokyo";
  if (name.includes("韩国")) return "Asia/Seoul";
  if (name.includes("新加坡")) return "Asia/Singapore";
  if (name.includes("泰国")) return "Asia/Bangkok";
  if (name.includes("英国")) return "Europe/London";
  if (name.includes("法国")) return "Europe/Paris";
  if (name.includes("德国")) return "Europe/Berlin";
  if (name.includes("纽约")) return "America/New_York";
  if (name.includes("洛杉矶") || name.includes("旧金山")) return "America/Los_Angeles";
  if (name.includes("多伦多")) return "America/Toronto";
  if (name.includes("温哥华")) return "America/Vancouver";
  if (name.includes("悉尼")) return "Australia/Sydney";
  if (name.includes("墨尔本")) return "Australia/Melbourne";
  if (name.includes("香港")) return "Asia/Hong_Kong";
  if (name.includes("澳门")) return "Asia/Macau";
  if (name.includes("台湾")) return "Asia/Taipei";
  return "Asia/Shanghai";
}

function normalizeChinaGeoRow(row) {
  const latitude = Number(row.lat);
  const longitude = Number(row.lng);
  if (!Number.isFinite(latitude) || !Number.isFinite(longitude)) return null;

  const province = String(row.province || "").trim();
  const city = String(row.city || "").trim();
  const area = String(row.area || "").trim();
  if (!province && !city && !area) return null;

  const parent = city === "市辖区" || !city ? province : city;
  const isCityLevel = !area;
  const name = isCityLevel
    ? `${parent}，中国`
    : `${area}，${parent}${province && province !== parent ? `，${province}` : ""}，中国`;

  return {
    name,
    keywords: [province, city, area, parent].filter(Boolean),
    latitude,
    longitude,
    timezone: 8,
    timeZone: "Asia/Shanghai",
    level: isCityLevel ? "地级市/州/盟/地区" : "区县",
    source: "city-geo",
  };
}

function dedupePlaces(places) {
  const seen = new Map();
  for (const place of places) {
    if (!seen.has(place.name)) {
      seen.set(place.name, place);
    }
  }
  return [...seen.values()].sort((a, b) => {
    const levelA = a.level === "地级市/州/盟/地区" ? 0 : a.level === "区县" ? 1 : 0;
    const levelB = b.level === "地级市/州/盟/地区" ? 0 : b.level === "区县" ? 1 : 0;
    return levelA - levelB || a.name.localeCompare(b.name, "zh-Hans-CN");
  });
}
